#include <iostream>
#include <cmath>
using namespace std;
main()
{
	cout << sqrt(64) << endl;
	cout << abs(-64) << endl;
	cout << pow(2,10) << endl;
  
	cout << floor(3.6) << endl;
	cout << ceil(3.6) << endl;
	cout << trunc(3.6) << endl;

	cout << log10(100) << endl;
	cout << log2(32) << endl;

	cout << fmod(17,3) << endl;
	 
}
